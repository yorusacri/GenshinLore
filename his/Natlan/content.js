年轻的讯使（布基纳）曾与山后的龙（康加玛托）赌气，因后者常常嗤笑他动作的痴愚，于是发下誓言，决心前往至高的山巅，采回母树结出的火榴果，来向即便在众匿叶龙中也最为灵敏的那位，展露自己的好本领。

