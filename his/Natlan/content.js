·松伽塔被姆班黛选为勇士

松伽塔年幼时，回声之子由姆班黛掌管，黑玉般的姆班黛曾是矿山与宝石的主母，她选中年轻的松伽塔，作为部族的勇士。那伟大的英雄与矿工，其故事就此开始。

于是，少年的松伽塔开始了矿工的生涯，开山的铁锤，都显得比他身躯还要庞大。然而强壮的少年在矿洞中挥舞铁锤如风，惊得勇士们急忙喊叫「矿洞马上要坍塌！」
