·图帕克的冒险

当烈风吹过泉流汇聚的谷地，曾有如山的巨人屹立在海天之际，砂岩般的躯壳下燃烧着炽热之心，蒸腾起的雾气似要遮蔽天日。众人皆知，那是梅兹特利的图帕克，生民以来没人比他更高大，从山到海，从丛林到原野，纳塔的人众中，无人可当他的对手。

